# to check if the three numbers form a pythagorean triplet

def check_triplet(a,b,c):
    if a**2 +b**2 == c**2:
        print("it is a pythagorean triplet")
    else:
        print("it is not a pythagorean triplet ")
        
check_triplet(3,4,5)


